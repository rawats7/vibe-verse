const playlist = document.getElementById("playlist");
const audio = document.getElementById("audio");
const progressBar = document.getElementById("progressBar");
const volume = document.getElementById("volume");
const timeDisplay = document.getElementById("time");
const searchInput = document.getElementById("searchInput");
const notFoundMessage = document.getElementById("notFoundMessage");

const playPauseBtn = document.getElementById("playPauseBtn");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");

let currentIndex = 0;
let isPlaying = false;

// ---------- Render Playlist ----------
function renderSongs(songList) {
    playlist.innerHTML = "";
    songList.forEach((song, idx) => {
        const div = document.createElement("div");
        div.className = "song";
        div.innerHTML = `
            <div style="display:flex;align-items:center;flex:1;">
                <img src="${song.cover}">
                <div class="song-info">
                    <div class="song-title">${song.title}</div>
                    <div class="song-artist">${song.artist}</div>
                </div>
            </div>
            <button class="download-btn">⋮
                <div class="download-options">
                    <a href="${song.file}" download>
                        ⬇ Download
                    </a>
                </div>
            </button>
        `;
        // Song click
        div.querySelector(".song-info").parentElement.onclick = (e) => {
            if (!e.target.classList.contains("download-btn")) {
                selectSong(idx);
            }
        };

        
        // Download button hover toggle
        const downloadBtn = div.querySelector(".download-btn");
        const options = div.querySelector(".download-options");
        downloadBtn.addEventListener("click", (e) => {
            e.stopPropagation();
            options.style.display = options.style.display === "block" ? "none" : "block";
        });
        playlist.appendChild(div);
    });
}

// ---------- Load & Play ----------
function loadSong() {
    const song = songs[currentIndex];
    audio.src = song.file;
    document.getElementById("title").innerText = song.title;
    document.getElementById("artist").innerText = song.artist;
    document.getElementById("cover").src = song.cover;

    // Highlight active song
    document.querySelectorAll(".song").forEach((s, idx) => {
        s.classList.toggle("active", idx === currentIndex);
    });
}

function playSong() {
    audio.play();
    isPlaying = true;
    playPauseBtn.innerText = "⏸";
}

function pauseSong() {
    audio.pause();
    isPlaying = false;
    playPauseBtn.innerText = "▶";
}

// ---------- Toggle Play/Pause ----------
playPauseBtn.addEventListener("click", () => {
    if (isPlaying) pauseSong();
    else playSong();
});

// ---------- Previous / Next ----------
prevBtn.addEventListener("click", () => {
    currentIndex = (currentIndex - 1 + songs.length) % songs.length;
    loadSong();
    playSong();
});

nextBtn.addEventListener("click", () => {
    currentIndex = (currentIndex + 1) % songs.length;
    loadSong();
    playSong();
});

// ---------- Select Song ----------
function selectSong(idx) {
    currentIndex = idx;
    loadSong();
    playSong();
}

// ---------- Progress Bar ----------
audio.addEventListener("timeupdate", () => {
    if (audio.duration) {
        const percent = (audio.currentTime / audio.duration) * 100;
        progressBar.style.width = percent + "%";
        const min = Math.floor(audio.currentTime / 60);
        const sec = Math.floor(audio.currentTime % 60).toString().padStart(2, "0");
        const durMin = Math.floor(audio.duration / 60);
        const durSec = Math.floor(audio.duration % 60).toString().padStart(2, "0");
        timeDisplay.innerText = `${min}:${sec} / ${durMin}:${durSec}`;
    }
});

function seek(e) {
    const width = e.currentTarget.clientWidth;
    audio.currentTime = (e.offsetX / width) * audio.duration;
}

// ---------- Volume ----------
volume.addEventListener("input", () => (audio.volume = volume.value));

// ---------- Autoplay ----------
window.addEventListener("load", () => {
    loadSong();
    playSong();
});

// ---------- Stop audio when user interacts elsewhere ----------
document.body.addEventListener("click", (e) => {
    if (!e.target.closest(".player") && !e.target.closest(".song")) {
        pauseSong();
    }
});

// ---------- Search Functionality ----------
searchInput.addEventListener("input", function () {
    const value = this.value.toLowerCase();
    const filtered = songs.filter(
        (song) =>
            song.title.toLowerCase().includes(value) ||
            song.artist.toLowerCase().includes(value)
    );

    if (filtered.length === 0) {
        playlist.innerHTML = "";
        notFoundMessage.style.display = "block";
    } else {
        notFoundMessage.style.display = "none";
        renderSongs(filtered);
    }
});

// ---------- Initial Render ----------
renderSongs(songs);

// ---------- Next Song Function ----------
function nextSong() {
    currentIndex = (currentIndex + 1) % songs.length; // loop to first after last
    loadSong();
    audio.play(); // directly play next song
    isPlaying = true;
    playPauseBtn.innerText = "⏸";
}





